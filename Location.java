public class Location {
    //attitudes
    int row = 0;
    int colum = 0;

    //constructer
    public Location(int one, int two) {
        row = one;
        colum = two;
    }

    //methods
    public int getRow() {
        return row;
    }

    public int getColum() {
        return colum;
    }
}
  